using Microsoft.AspNetCore.Mvc;
using System;
using System.Collections.Generic;
using System.Threading.Tasks;
using TestApp.ToDoList.Entity;
using ToDoList.Tracker;

namespace TestApp.ToDoList.Module
{
  /// <summary>
  /// Tracking to-do list items.
  /// </summary>
  public interface IToDoListTracker
  {
    /// <summary>
    /// Adds a new to-do item.
    /// </summary>
    /// <param name="title"></param>
    /// <returns></returns>
    ToDoItem AddItem(string title);
    /// <summary>
    /// Adds a new to-do item.
    /// </summary>
    /// <param name="title"></param>
    /// <param name="tags"></param>
    /// <returns></returns>
    ToDoItem AddItem(string title, List<string> tags);
    /// <summary>
    /// Removes a to-do item.
    /// </summary>
    /// <param name="id"></param>
    /// <returns></returns>
    ToDoItem RemoveItem(int id);
    /// <summary>
    /// Gets a to-do item by its Id.
    /// </summary>
    /// <param name="id"></param>
    /// <returns></returns>
    ToDoItem GetItem(int id);
    /// <summary>
    /// Gets all to-do items.
    /// </summary>
    /// <returns></returns>
    IEnumerable<ToDoItem> GetAllItems();
    /// <summary>
    /// Gets all to-do items in async mode.
    /// </summary>
    /// <returns></returns>
    Task<ActionResult<IEnumerable<ToDoItem>>> GetAllItemsAsync();
    /// <summary>
    /// Edits a to-do item.
    /// </summary>
    /// <param name="id"></param>
    /// <param name="updatedTask"></param>
    /// <returns></returns>
    ToDoItem EditItem(int id, ToDoItem updatedTask);
    /// <summary>
    /// Gets one page of to-do items.
    /// </summary>
    /// <param name="skip"></param>
    /// <param name="pageSize"></param>
    /// <returns></returns>
    Task<ActionResult<IEnumerable<ToDoItem>>> GetOnePageAsync(int skip, int pageSize);
    /// <summary>
    /// Gets the filtered and sorted to-do items.
    /// </summary>
    /// <param name="filter"></param>
    /// <param name="orderBy"></param>
    /// <returns></returns>
    //Task<ActionResult<IEnumerable<ToDoItem>>> GetFilteredAndSortedItemsAsync(Func<ToDoItem, bool> filter, Func<ToDoItem, DateTime> orderBy);
    ///// <summary>
    ///// Gets the filtered and sorted to-do items.
    ///// </summary>
    ///// <param name="filterParameters">filter parameters</param>
    ///// <returns></returns>
    Task<ActionResult<IEnumerable<ToDoItem>>> GetFilteredAndSortedItemsAsync(FilterParameters filterParameters, string? sortColumn = "Title");

  }
}